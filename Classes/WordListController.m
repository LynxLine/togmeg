#import <sqlite3.h>
#import "WordListController.h"
#import "WordViewController.h"

static int WordsSelectCallback(void * context, int count, char **values, char **columns)
{
    NSMutableArray * words = (NSMutableArray *)context;
    for (int i=0; i< count;i++) {
        const char *wordCString = values[i];
        [words addObject:[NSString stringWithUTF8String:wordCString]];
    }
    return SQLITE_OK;
}

@implementation WordListController

@synthesize navigationController;

#pragma mark -
#pragma mark View lifecycle

- (void)loadWordsFromDatabase
{
    NSString * file = [[NSBundle mainBundle] pathForResource:@"BasicEnglish" ofType:@"db"];
    sqlite3 *database = NULL;
    if (sqlite3_open([file UTF8String], &database) == SQLITE_OK) {
        sqlite3_exec(database, "SELECT word FROM word", WordsSelectCallback, words, NULL);
    }
    sqlite3_close(database);
}

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        words = [[NSMutableArray alloc] init];
        [self loadWordsFromDatabase];
    }
    return self;
}

